import java.util.*;

public class valueofNumber {
    public static void main(String[] args){
        Scanner s=new Scanner(System.in);
        int a;
        System.out.print("ENTER A NUMBER : ");
        a=s.nextInt();
        if(a==0){
            System.out.println("NUMBER IS ZERO");
        }
        else if(a<0){
            System.out.println("NUMBER IS NEGATIVE");
        }
        else{
            System.out.println("NUMBER IS NEGATIVE");
        }
    }
}
